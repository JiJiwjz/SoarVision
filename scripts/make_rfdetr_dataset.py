"""
Adapt the type-first YOLO dataset into the split-first layout RF-DETR expects.

RF-DETR auto-detects a YOLO dataset from this structure (note: `valid`, not `val`,
and per-split images/ + labels/):

    <out>/
    ├── data.yaml                 # names (0-indexed list) + nc + train/val/test
    ├── train/{images,labels}/
    ├── valid/{images,labels}/
    └── test/{images,labels}/

Our source dataset is the opposite (type-first): images/<split>, labels/<split>.
Rather than copy ~6.5 GB, we symlink the per-split image/label directories into
the new layout and write a data.yaml. Classes stay 3: vessel / small_boat / buoy
(buoy kept — it's an official target and a small safety-relevant object).

Usage
-----
    python scripts/make_rfdetr_dataset.py                      # symlink adapter (default)
    python scripts/make_rfdetr_dataset.py --link-mode copy     # real copies (portable, heavy)
    python scripts/make_rfdetr_dataset.py --out datasets/maritime_rfdetr
"""

from __future__ import annotations

import argparse
import os
import shutil
from pathlib import Path

from _common import CLASS_NAMES, DATASET_DIR, IMG_EXTS, REPO_ROOT

# RF-DETR wants `valid`; our source split is `val`.
SPLIT_MAP = {"train": "train", "val": "valid", "test": "test"}


def _link_dir(target: Path, link: Path, mode: str) -> None:
    """Make `link` resolve to `target` via symlink (default) / copy / hardlink-tree."""
    if link.is_symlink() or link.exists():
        if link.is_dir() and not link.is_symlink():
            shutil.rmtree(link)
        else:
            link.unlink()
    if mode == "symlink":
        rel = os.path.relpath(target.resolve(), link.parent.resolve())
        link.symlink_to(rel, target_is_directory=True)
    elif mode == "copy":
        shutil.copytree(target, link)
    elif mode == "hardlink":
        link.mkdir(parents=True, exist_ok=True)
        for f in target.iterdir():
            os.link(f, link / f.name)
    else:
        raise ValueError(f"unknown link mode: {mode}")


def write_data_yaml(out: Path) -> Path:
    names_block = "\n".join(f"- {n}" for n in CLASS_NAMES)
    yaml = (
        "# RF-DETR YOLO dataset (format auto-detected). "
        "Generated by scripts/make_rfdetr_dataset.py — do not edit by hand.\n"
        f"names:\n{names_block}\n"
        f"nc: {len(CLASS_NAMES)}\n"
        "train: train/images\n"
        "val: valid/images\n"   # key is `val`, folder is `valid/`
        "test: test/images\n"
    )
    path = out / "data.yaml"
    path.write_text(yaml)
    return path


def count_images(folder: Path) -> int:
    if not folder.exists():
        return 0
    return sum(1 for p in folder.iterdir() if p.suffix.lower() in IMG_EXTS)


def main() -> int:
    ap = argparse.ArgumentParser(description=__doc__, formatter_class=argparse.RawDescriptionHelpFormatter)
    ap.add_argument("--src", default=str(DATASET_DIR), help="source type-first YOLO dataset")
    ap.add_argument("--out", default=str(REPO_ROOT / "datasets" / "maritime_rfdetr"),
                    help="destination split-first RF-DETR dataset")
    ap.add_argument("--link-mode", default="symlink", choices=["symlink", "copy", "hardlink"])
    args = ap.parse_args()

    src = Path(args.src).resolve()
    out = Path(args.out).resolve()
    out.mkdir(parents=True, exist_ok=True)

    total = 0
    for src_split, dst_split in SPLIT_MAP.items():
        src_images = src / "images" / src_split
        src_labels = src / "labels" / src_split
        if not src_images.is_dir():
            print(f"[adapt] WARN missing {src_images}, skipping {src_split}")
            continue
        dst_dir = out / dst_split
        dst_dir.mkdir(parents=True, exist_ok=True)
        _link_dir(src_images, dst_dir / "images", args.link_mode)
        _link_dir(src_labels, dst_dir / "labels", args.link_mode)
        n = count_images(dst_dir / "images")
        total += n
        print(f"[adapt] {src_split:>5} -> {dst_split:<5}  {n:>6} images  ({args.link_mode})")

    yaml_path = write_data_yaml(out)
    print(f"\n[adapt] classes ({len(CLASS_NAMES)}): {CLASS_NAMES}")
    print(f"[adapt] data.yaml -> {yaml_path}")
    print(f"[adapt] done: {total} images linked under {out}")
    print(f"[adapt] train RF-DETR with: dataset_dir='{out}'")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
